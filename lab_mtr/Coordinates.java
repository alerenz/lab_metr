package com.example.lab_mtr;

public class Coordinates {

        private float x;
        private float y;
        private float z;

        public Coordinates(float x, float y, float z) {
            this.x = x;
            this.y = y;
            this.z = z;
        }

        public float getX() {
            return x;
        }

        public float getY() {
            return y;
        }

        public float getZ() {
            return z;
        }

}
